package gr.aueb.cf.cf9.ch4;

public class StarHorizontalApp {
    public static void main(String[] args) {
        //HorizontalStars
        for (int i = 1; i <= 10 ; i++)
        System.out.print("*");
        //VerticalStars
        for (int i = 1; i <= 10 ; i++)
            System.out.println("*");
        //10*10
        for (int i = 1; i <= 10 ; i++){
            for (int j = 1; j <= 10 ; j++){
                System.out.print("*");}
            System.out.println("");

        }

    }
}
